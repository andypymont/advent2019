QUnit.test('tractor_beam(program)(x, y)', function(assert) {
  const cases = [
    [0, 0, 1],
    [1, 1, 1],
    [1, 2, 0],
    [8, 5, 1],
    [8, 7, 1],
  ]
  const tractor = tractor_beam(test_program)
  cases.forEach(function([x, y, expected]) {
    const desc = [
      'tractor_beam(test_program)(',
      x,
      ', ',
      y,
      ') === ',
      expected
    ].join('')
    assert.equal(tractor(x, y), expected, desc)
  })
})

QUnit.test('area_covered(tractor_beam, size)', function(assert) {
  const tractor = tractor_beam(test_program)
  assert.equal(area_covered(tractor, 10),
               27,
               'area_covered(tractor_beam(test_program), 10) === 27')
})

QUnit.test('locate_square(tractor_beam, size)', function(assert) {
  const tractor = tractor_beam(test_program)
  const expected = { 'x': 25, 'y': 20, 'solution': 250020 }
  assert.deepEqual(locate_square(tractor, 10),
                   expected,
                   'locate_square(tractor_beam(test_program)) gives x=25, y=20')
})

QUnit.test('Solutions', async function(assert) {
  const program = await fetch_puzzle_input().then(read_program)
  const tractor = tractor_beam(program)
  assert.equal(area_covered(tractor, 50),
               176,
               'Part 1: area affected by tractor beam = 176')
  assert.equal(locate_square(tractor, 100).solution,
               6751081,
               'Part 2: location of 100x100 square = 6751081')
})
